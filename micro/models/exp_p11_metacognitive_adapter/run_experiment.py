#!/usr/bin/env python3
"""
P11.H1: thinking-metar1-metacognitive-v0 (Sequential Adapter Composition)

Trains a metacognitive LoRA adapter starting from thinking-universal-v0 (P11.H0).
Uses H0 adapter to generate higher-quality metacognitive training traces, then
fine-tunes further with PLAN/CHECK format injection.

Key design: H1 = thinking-universal (H0) checkpoint + metacognitive SFT
- Single adapter (no runtime composition overhead)
- Training data generated by H0 (higher accuracy than base model)
- Format injection post-hoc (PLAN/CHECK) — does NOT hurt generation yield

Kill criteria:
  K1520: Thinking chars reduced >= 20% vs thinking-universal-v0
         (H0 ~3202 chars → H1 ≤ 2562 chars avg)
  K1521: MMLU-Pro accuracy >= thinking-universal-v0 (H0)
  K1522: >= 50% thinking traces contain explicit plan structure

References:
  arXiv:2508.17291 (Meta-R1: metacognitive framework, +27.3% SOTA)
  arXiv:2502.03387 (LIMO: quality > quantity for reasoning SFT)
  arXiv:2402.07148 (LoRA+: sequential LoRA composition)
  exp_p11_thinking_adapter_universal: H0 MATH.md + adapter
  exp_p11_meta_r1_metacognition: P11.D0 metacognitive protocol
"""

