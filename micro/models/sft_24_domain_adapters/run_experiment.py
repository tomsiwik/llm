#!/usr/bin/env python3
"""SFT retrain 24 domain adapters on BitNet-2B with Grassmannian A.

Replicates the proven SFT training recipe from bitnet_sft_generation_v3
(Finding #187: SFT > NTP on 4/5 domains) but for all 24 domains.

Training recipe (exact match of proven setup):
  - Rank 16, scale 20.0, 300 steps, lr 1e-4, Adam
  - SFT loss: cross-entropy on response tokens only (instruction masked)
  - TernaryLoRALinear: ternary B via STE, Grassmannian A frozen
  - BitLinear unpacked to nn.Linear for differentiable training

Kill criteria:
  K750: Fewer than 15/24 domains show PPL improvement over NTP baseline
  K752: Any domain adapter fails to converge
"""

import gc
import json
import math
import os
import time
from pathlib import Path

os.environ["TOKENIZERS_PARALLELISM"] = "false"

import numpy as np
import mlx.core as mx
import mlx.nn as nn
import mlx.optimizers as opt
from mlx.utils import tree_flatten, tree_unflatten

device_info = mx.device_info()
mx.set_memory_limit(device_info["memory_size"] - 8 * 1024**3)
mx.set_cache_limit(2 * 1024**3)

from mlx_lm import load
from mlx_lm.models.bitlinear_layers import BitLinear

EXPERIMENT_DIR = Path(__file__).parent
RESULTS_FILE = EXPERIMENT_DIR / "results.json"
ADAPTERS_OUT = EXPERIMENT_DIR / "sft_adapters"

# Source data — 24 domains
SOURCE_DIR = EXPERIMENT_DIR.parent / "real_data_25_domain_adapters"
NTP_ADAPTERS_DIR = SOURCE_DIR / "adapters"
DATA_DIR = SOURCE_DIR / "data"
SKELETON_PATH = NTP_ADAPTERS_DIR / "grassmannian_skeleton_n24.npz"
NTP_RESULTS_PATH = SOURCE_DIR / "results.json"

MODEL_ID = "microsoft/BitNet-b1.58-2B-4T"
LORA_RANK = 16
LORA_SCALE = 20.0
TRAIN_ITERS = 300
LEARNING_RATE = 1e-4
MAX_SEQ = 256
SEED = 42

RESPONSE_MARKER = "### Response:\n"

TARGET_KEYS = [
    "self_attn.q_proj", "self_attn.k_proj", "self_attn.v_proj",
    "self_attn.o_proj", "mlp.gate_proj", "mlp.up_proj", "mlp.down_proj",
]

# CRITICAL: Domain ordering MUST match the Grassmannian skeleton indices.
# The skeleton was generated by the NTP experiment (real_data_25_domain_adapters)
# using its active_domains ordering, NOT alphabetical sort.
NTP_DOMAIN_ORDER = [
    "medical", "code", "math", "legal", "finance", "science", "history",
    "philosophy", "creative_writing", "cooking", "health_fitness", "psychology",
    "education", "engineering", "agriculture", "environmental", "politics",
    "economics", "sociology", "linguistics", "cybersecurity", "marketing",
    "sports", "music",
]


class NumpyEncoder(json.JSONEncoder):
    def default(self, o):
        if isinstance(o, (np.bool_,)): return bool(o)
        if isinstance(o, (np.integer,)): return int(o)
        if isinstance(o, (np.floating,)): return float(o)
        if isinstance(o, np.ndarray): return o.tolist()
        return super().default(o)

def log(m): print(m, flush=True)

def cleanup(*o):
    for x in o: del x
    gc.collect(); mx.clear_cache(); mx.reset_peak_memory()


# ── BitLinear unpacking (required for training) ─────────────────────────

def unpack_model(model):
    count = 0
    for layer in model.model.layers:
        updates = []
        for key, module in layer.named_modules():
            if isinstance(module, BitLinear):
                w = module.weight; s = module.weight_scale
                w0 = (w & 3).astype(mx.bfloat16) - 1
                w1 = ((w >> 2) & 3).astype(mx.bfloat16) - 1
                w2 = ((w >> 4) & 3).astype(mx.bfloat16) - 1
                w3 = ((w >> 6) & 3).astype(mx.bfloat16) - 1
                unpacked = mx.concatenate([w0, w1, w2, w3], axis=0)[:module.out_features]
                scale = s.astype(mx.bfloat16)
                unpacked = unpacked / scale if module.invert_weight_scales else unpacked * scale
                lin = nn.Linear(module.in_features, module.out_features, bias=module.bias is not None)
                lin.weight = unpacked
                if module.bias is not None: lin.bias = module.bias
                updates.append((key, lin)); count += 1
        if updates: layer.update_modules(tree_unflatten(updates))
    mx.eval(model.parameters())
    return model


# ── TernaryLoRA (exact match of proven recipe) ──────────────────────────

class TernaryLoRALinear(nn.Module):
    """LoRA with STE-ternary B and Grassmannian A. Proven in Finding #187."""
    def __init__(self, base_linear, rank=16, scale=20.0, a_init=None):
        super().__init__()
        in_f = base_linear.weight.shape[1]
        out_f = base_linear.weight.shape[0]
        self.linear = base_linear
        self.lora_a = a_init if a_init is not None else mx.random.uniform(
            low=-1.0/math.sqrt(in_f), high=1.0/math.sqrt(in_f), shape=(in_f, rank))
        self.lora_b = mx.zeros((rank, out_f))
        self.scale = scale
        self.linear.freeze()
        self.freeze(keys=["lora_a"], strict=False)

    def __call__(self, x):
        base_out = self.linear(x)
        b = self.lora_b
        alpha = mx.mean(mx.abs(b))
        b_scaled = b / (alpha + 1e-7)
        b_q = mx.clip(mx.round(b_scaled), -1, 1) * alpha
        b_ste = b + mx.stop_gradient(b_q - b)
        return base_out + (x @ self.lora_a) @ b_ste * self.scale


# ── SFT data loading ────────────────────────────────────────────────────

def load_domain_texts(domain, split="train", max_n=400):
    path = DATA_DIR / domain / f"{split}.jsonl"
    if not path.exists():
        return []
    texts = []
    with open(path) as f:
        for i, line in enumerate(f):
            if i >= max_n: break
            texts.append(json.loads(line)["text"])
    return texts


def tokenize_sft(text, tokenizer, max_len=256):
    """Tokenize with SFT mask: 1 for response tokens, 0 for instruction."""
    response_idx = text.find(RESPONSE_MARKER)
    if response_idx < 0:
        tokens = tokenizer.encode(text)[:max_len]
        return tokens, [1] * len(tokens)
    instruction_part = text[:response_idx + len(RESPONSE_MARKER)]
    instruction_len = len(tokenizer.encode(instruction_part))
    full_tokens = tokenizer.encode(text)[:max_len]
    mask = [0] * min(instruction_len, len(full_tokens))
    mask += [1] * (len(full_tokens) - len(mask))
    return full_tokens, mask


def sft_loss_fn(model, tokens, mask):
    logits = model(tokens[:, :-1])
    targets = tokens[:, 1:]
    response_mask = mask[:, 1:]
    per_token_loss = nn.losses.cross_entropy(logits, targets, reduction="none")
    masked_loss = per_token_loss * response_mask
    n_response = mx.maximum(response_mask.sum(), mx.array(1.0))
    return masked_loss.sum() / n_response


# ── Main training loop ──────────────────────────────────────────────────

def log_memory(label=""):
    """Print current memory usage."""
    active = mx.get_active_memory() / 1e9
    cache = mx.get_cache_memory() / 1e9
    peak = mx.get_peak_memory() / 1e9
    log(f"[MEM {label}] active={active:.2f}GB cache={cache:.2f}GB peak={peak:.2f}GB")


def phase_train_domain(di, domain, skeleton, ntp_ppls):
    """Train one SFT adapter. Self-contained for memory isolation."""
    dt0 = time.time()
    log(f"\n{'='*50}")
    log(f"Domain {di+1}/24: {domain} (skeleton index {di})")
    log(f"{'='*50}")

    # Load and unpack model
    model, tokenizer = load(MODEL_ID)
    model = unpack_model(model)

    # Apply TernaryLoRA with Grassmannian A
    n_lora = 0
    for li, layer in enumerate(model.model.layers):
        updates = []
        for key in TARGET_KEYS:
            m = layer
            for part in key.split("."):
                m = getattr(m, part, None)
                if m is None: break
            if m is None or not isinstance(m, nn.Linear): continue
            skey = f"layer_{li}_{key}_domain_{di}"
            a_mx = mx.array(skeleton[skey]).astype(mx.bfloat16) if skey in skeleton else None
            if a_mx is None:
                log(f"  WARNING: no skeleton key {skey}")
            lora = TernaryLoRALinear(m, rank=LORA_RANK, scale=LORA_SCALE, a_init=a_mx)
            updates.append((key, lora))
            n_lora += 1
        if updates:
            layer.update_modules(tree_unflatten(updates))
    mx.eval(model.parameters())

    model.freeze()
    model.unfreeze(keys=["lora_b"], strict=False)
    trainable = sum(p.size for _, p in tree_flatten(model.trainable_parameters()))
    log(f"  LoRA modules: {n_lora}, trainable params: {trainable:,}")

    # Load SFT data
    train_texts = load_domain_texts(domain, "train", 400)
    val_texts = load_domain_texts(domain, "valid", 50)
    if not train_texts:
        log(f"  SKIP: no training data")
        cleanup(model, tokenizer)
        return None

    train_batches = []
    for text in train_texts:
        tokens, mask = tokenize_sft(text, tokenizer, MAX_SEQ)
        if len(tokens) >= 4:
            train_batches.append((tokens, mask))

    val_batches = []
    for text in val_texts:
        tokens, mask = tokenize_sft(text, tokenizer, MAX_SEQ)
        if len(tokens) >= 4:
            val_batches.append((tokens, mask))

    log(f"  Data: {len(train_batches)} train, {len(val_batches)} val batches")

    # Baseline val loss (SFT loss on response tokens, no adapter active)
    base_loss = 0.0
    for tokens, mask in val_batches[:25]:
        loss = sft_loss_fn(model, mx.array([tokens]), mx.array([mask]))
        mx.eval(loss)
        base_loss += loss.item()
        del loss
    base_loss /= max(len(val_batches[:25]), 1)
    log(f"  Base val loss (SFT, no adapter): {base_loss:.4f}")

    # Train
    optimizer = opt.Adam(learning_rate=LEARNING_RATE)
    loss_and_grad = nn.value_and_grad(model, sft_loss_fn)
    initial_loss = None

    gc.disable()
    for step in range(TRAIN_ITERS):
        idx = step % len(train_batches)
        tokens, mask = train_batches[idx]
        loss, grads = loss_and_grad(model, mx.array([tokens]), mx.array([mask]))
        optimizer.update(model, grads)
        mx.eval(model.parameters(), optimizer.state, loss)
        lv = loss.item()
        if initial_loss is None:
            initial_loss = lv
        if (step + 1) % 100 == 0:
            log(f"  Step {step+1}: loss={lv:.4f}")
    gc.enable()

    # Final val loss
    final_loss = 0.0
    for tokens, mask in val_batches[:25]:
        loss = sft_loss_fn(model, mx.array([tokens]), mx.array([mask]))
        mx.eval(loss)
        final_loss += loss.item()
        del loss
    final_loss /= max(len(val_batches[:25]), 1)

    improved = final_loss < base_loss
    pct_change = (base_loss - final_loss) / base_loss * 100 if base_loss > 0 else 0

    log(f"  Val loss: {base_loss:.4f} -> {final_loss:.4f} ({pct_change:+.1f}%, {'improved' if improved else 'degraded'})")

    # Save adapter B-matrices
    adapter_dir = ADAPTERS_OUT / domain
    adapter_dir.mkdir(parents=True, exist_ok=True)
    adapter_params = {}
    for name, param in tree_flatten(model.trainable_parameters()):
        if "lora_b" in name:
            adapter_params[name] = param
    mx.savez(str(adapter_dir / "adapter.npz"), **adapter_params)
    log(f"  Saved {len(adapter_params)} params to {adapter_dir}/adapter.npz")

    elapsed = time.time() - dt0
    ntp_ppl = ntp_ppls.get(domain)
    result = {
        "base_loss": round(base_loss, 4),
        "final_loss": round(final_loss, 4),
        "initial_train_loss": round(initial_loss, 4) if initial_loss else None,
        "improved_over_base": improved,
        "pct_improvement": round(pct_change, 2),
        "ntp_ppl": round(ntp_ppl, 4) if ntp_ppl else None,
        "train_time_s": round(elapsed, 1),
        "n_train": len(train_batches),
        "n_lora": n_lora,
    }

    log_memory(f"post-{domain}")
    cleanup(model, tokenizer, optimizer)
    return result


def main():
    t0 = time.time()
    log("SFT Retrain: 24 Domain Adapters")
    log("=" * 60)
    mx.random.seed(SEED)
    log_memory("start")

    ADAPTERS_OUT.mkdir(parents=True, exist_ok=True)

    # Load Grassmannian skeleton
    skeleton = dict(np.load(str(SKELETON_PATH)))
    log(f"Skeleton: {len(skeleton)} keys from {SKELETON_PATH}")

    # Load NTP PPLs for comparison
    ntp_ppls = {}
    if NTP_RESULTS_PATH.exists():
        ntp_results = json.load(open(NTP_RESULTS_PATH))
        ntp_ppls = ntp_results.get("individual_ppls", {})
        log(f"NTP baseline PPLs loaded for {len(ntp_ppls)} domains")

    # Use NTP domain ordering to match skeleton indices
    domains = NTP_DOMAIN_ORDER
    log(f"Domains ({len(domains)}): {domains}")

    # Verify all domains have data
    for d in domains:
        data_path = DATA_DIR / d / "train.jsonl"
        if not data_path.exists():
            log(f"  WARNING: {d} has no train.jsonl")

    results = {"per_domain": {}, "summary": {}}
    improved_count = 0
    skipped = []

    for di, domain in enumerate(domains):
        domain_result = phase_train_domain(di, domain, skeleton, ntp_ppls)
        if domain_result is None:
            skipped.append(domain)
            continue
        results["per_domain"][domain] = domain_result
        if domain_result["improved_over_base"]:
            improved_count += 1

    # Summary
    total_time = time.time() - t0
    n_trained = len(results["per_domain"])
    results["summary"] = {
        "total_domains": len(domains),
        "trained": n_trained,
        "skipped": skipped,
        "improved": improved_count,
        "total_time_s": round(total_time, 1),
        "total_time_min": round(total_time / 60, 1),
        "mean_pct_improvement": round(
            np.mean([d["pct_improvement"] for d in results["per_domain"].values()]), 2
        ),
    }

    # Kill criteria
    k750 = improved_count >= 15
    diverged_domains = []
    for d_name, d in results["per_domain"].items():
        if d.get("initial_train_loss") and d["final_loss"] > 2 * d["initial_train_loss"]:
            diverged_domains.append(d_name)
    k752 = len(diverged_domains) == 0

    results["kill_criteria"] = {
        "K750": {"pass": k750, "value": improved_count, "threshold": 15},
        "K752": {"pass": k752, "detail": f"diverged: {diverged_domains}" if diverged_domains else "no divergence"},
    }
    results["all_pass"] = k750 and k752

    log(f"\n{'='*60}")
    log(f"RESULTS")
    log(f"{'='*60}")
    log(f"Trained: {n_trained}/{len(domains)}")
    log(f"Improved (SFT loss < base): {improved_count}/{n_trained}")
    log(f"Mean improvement: {results['summary']['mean_pct_improvement']:.1f}%")
    log(f"Total time: {total_time/60:.1f} min")

    # Per-domain summary table
    log(f"\n{'Domain':<20s} {'Base':>8s} {'SFT':>8s} {'Impr%':>8s} {'NTP PPL':>8s} {'Time':>6s}")
    log("-" * 58)
    for domain in domains:
        if domain in results["per_domain"]:
            d = results["per_domain"][domain]
            ntp_str = f"{d['ntp_ppl']:.2f}" if d.get("ntp_ppl") else "N/A"
            log(f"{domain:<20s} {d['base_loss']:8.4f} {d['final_loss']:8.4f} "
                f"{d['pct_improvement']:+7.1f}% {ntp_str:>8s} {d['train_time_s']:5.0f}s")

    log(f"\nKill Criteria:")
    for k, v in results["kill_criteria"].items():
        log(f"  {k}: {'PASS' if v['pass'] else 'FAIL'} -- {v}")
    log(f"\n{'ALL PASS' if results['all_pass'] else 'KILLED'}")

    log_memory("final")
    RESULTS_FILE.write_text(json.dumps(results, indent=2, cls=NumpyEncoder))

if __name__ == "__main__":
    main()
