#!/usr/bin/env python3
"""
exp_hedgehog_adapter_sql_domain — Hedgehog per-layer cos-sim distillation for SQL
query-optimization domain knowledge.

Pre-registered KCs (canonical DB text — do not edit):
  K#1868 proxy:  SQL adapter PPL on SQL-specific eval > base + generic LoRA
                 (kill if TRUE — Hedgehog strictly worse)
  K#1869 target: SQL query correctness auto-judge < +5pp vs base
                 (pair K1868 per F#666)  (kill if TRUE — target threshold not met)

Skills invoked: /mlx-dev (documented in MATH.md §0). /fast-mlx pending for the _impl
follow-up (where the real training loop lands).

Runtime structure (mirrors sibling exp_hedgehog_adapter_rust_domain):
  Phase 0 — PostgreSQL docs + Winand + SQL-optimization guide corpus + (Q, A) pair
            curation
  Phase A — teacher attention-output capture: 26B Gemma variant + π_Sql in context + Q
  Phase B — student training: rank-8 LoRA on v_proj/o_proj, per-layer cos-sim loss
            (custom MLX training loop — NOT available via mlx_lm.lora CLI)
  Phase Baseline — generic token-space LoRA at matched params (for K1868 head-to-head)
  Phase C — K1868 PPL on held-out SQL eval (base vs base+gen-LoRA vs base+Hedgehog)
  Phase D — K1869 query-correctness auto-judge on 50 blind-paired prompts (+ PostgreSQL
            dry-run EXPLAIN as hard-floor for syntactic+semantic validity)

Full budget: ~4–6 h on M5 Pro 48 GB (two training jobs at 800 steps + 26B teacher
residency + judge per held-out pair + PPL eval). Exceeds single-iteration researcher cap
(30 min / 40 tool calls per guardrail 1009). This scaffold supports SMOKE_TEST=1 and
full runs; Phase 0/B/Baseline/C/D raise NotImplementedError pending a dedicated `_impl`
iteration (antipattern `mem-antipattern-novel-mechanism-single-iteration-scope`).
"""

from __future__ import annotations

import gc
import json
import os
import time
from pathlib import Path

import mlx.core as mx

# M5 Pro 48 GB: leave 8 GB headroom; cap wired cache at 2 GB per mlx-dev guidance.
mx.set_memory_limit(mx.device_info()["memory_size"] - 8 * 1024**3)
mx.set_cache_limit(2 * 1024**3)

EXPERIMENT_DIR = Path(__file__).parent
RESULTS_FILE = EXPERIMENT_DIR / "results.json"

STUDENT_MODEL = "mlx-community/gemma-4-e4b-it-4bit"
TEACHER_MODEL = "mlx-community/gemma-4-26b-a4b-it-4bit"

IS_SMOKE = os.environ.get("SMOKE_TEST", "0") == "1"
N_TRAIN = 40 if IS_SMOKE else 200
N_HELDOUT = 10 if IS_SMOKE else 50
N_STEPS = 50 if IS_SMOKE else 800
SEED = 42

ADAPTER_RANK = 8
ADAPTER_TARGETS = ("v_proj", "o_proj")  # F#627
LORA_SCALE = 6.0                        # <= 8 per F#328/F#330
BATCH_SIZE = 1
SEQLEN = 2048                           # PostgreSQL docs + Winand blocks run longer than snippets
ENABLE_THINKING = True                  # F#614 — load-bearing on Gemma 4 reasoning

SQL_FOCUS_TOPICS = (
    "join_strategies_and_order",
    "index_selection_and_design",
    "subquery_and_cte_optimization",
    "window_functions_and_frame_clauses",
    "aggregation_and_grouping",
    "query_plan_reading_and_cost_model",
    "statistics_and_analyze",
    "transaction_and_isolation",
)


def log_memory(label=""):
    active = mx.get_active_memory() / 1e9
    cache = mx.get_cache_memory() / 1e9
    print(f"[MEM {label}] active={active:.2f}GB cache={cache:.2f}GB", flush=True)


def cleanup(*objects):
    for obj in objects:
        del obj
    gc.collect()
    mx.clear_cache()
    mx.reset_peak_memory()


# ─────────────────────────────────────────────
# Phase 0: Dataset
# ─────────────────────────────────────────────

def prepare_sql_qa_pairs(data_dir: Path, n_train: int, n_heldout: int) -> dict:
    """Curate (topic, π_Sql, Q, A) pairs for the 8 focus topics.

    NOT YET IMPLEMENTED. Requires:
      - PostgreSQL docs chapter excerpts (fair-use summaries) for each of
        SQL_FOCUS_TOPICS (Planner/Optimizer, Indexes, Performance Tips, etc.).
      - Winand "Use the Index, Luke" excerpts (fair-use summaries, CC-BY-NC-ND).
      - Spider 2.0 + BIRD-SQL optimization-relevant slices (training portions,
        disjoint from held-out PPL eval).
      - N_TRAIN + N_HELDOUT stratified (Q, A) pairs per topic, generated by a larger
        model with source-in-context.

    The _impl follow-up (exp_hedgehog_adapter_sql_domain_impl) landed inline at P=3
    should implement this.
    """
    raise NotImplementedError(
        "Phase 0 dataset curation requires PostgreSQL docs + Winand + Spider 2.0 / "
        "BIRD-SQL corpus preparation. Deferred to _impl follow-up."
    )


# ─────────────────────────────────────────────
# Phase A+B: Hedgehog teacher capture + student training
# ─────────────────────────────────────────────

def train_hedgehog_student(data_dir: Path, adapter_path: Path) -> dict:
    """Phase A (teacher attention capture) + Phase B (student training with per-layer
    cos-sim loss on attention outputs).

    NOT YET IMPLEMENTED. Requires:
      - Load 26B teacher with π_Sql + Q in context. Capture {layer_idx: attn_output}
        for all 42 layers. Sequential-phase eviction on 48 GB M5 Pro per F#673
        (teacher forward → cache → evict → student forward).
      - Student rank-8 LoRA on (v_proj, o_proj) with per-layer cos-sim loss:
        L = mean_l (1 − cos(A_teacher_l, A_student_l)).
      - Custom MLX training loop — NOT available via mlx_lm.lora CLI (which only
        supports next-token CE).
      - nn.value_and_grad(student, loss_fn) + AdamW. mx.eval + mx.clear_cache between
        batches (F#673).
      - 800 steps at BATCH_SIZE=1, SEQLEN=2048.

    Peak memory is load-bearing on 48 GB; sequential-phase eviction or offline
    precompute-to-disk of teacher traces required.
    """
    raise NotImplementedError(
        "Hedgehog training loop requires custom MLX training with per-layer attention "
        "hooks (not available via mlx_lm.lora CLI). Uses nn.value_and_grad + AdamW. "
        "Teacher is 26B — peak memory is load-bearing. "
        "See PAPER.md §Measurement blockers."
    )


def train_generic_token_lora_baseline(data_dir: Path, adapter_path: Path) -> dict:
    """Baseline for K1868: generic token-space LoRA next-token CE on (Q -> A) pairs at
    matched rank/targets/scale/steps. This IS available via mlx_lm.lora CLI — but
    running it in isolation without the Hedgehog arm would produce an unpaired K1868
    measurement (PPL comparison loses its head-to-head anchor). Deferred to the _impl
    follow-up so both arms land together.
    """
    raise NotImplementedError(
        "Generic token-space LoRA baseline deferred to _impl follow-up to keep K1868 "
        "head-to-head PPL measurement paired with Hedgehog arm."
    )


# ─────────────────────────────────────────────
# Phase C: K1868 PPL head-to-head
# ─────────────────────────────────────────────

def measure_k1868_ppl(student_adapter: Path, baseline_adapter: Path,
                      sql_eval_text: list) -> dict:
    """K1868: PPL(base), PPL(base + generic token-space LoRA), PPL(base + Hedgehog).

    Kill condition: PPL(Hedgehog) > PPL(base + generic token-space LoRA) strictly.

    NOT YET IMPLEMENTED. Requires:
      - mlx_lm.load(STUDENT_MODEL, adapter_path=...) for each of the three configs.
      - For each config, compute cross-entropy on tokenized held-out sql_eval_text
        (external Spider 2.0 + BIRD-SQL test-split + pgbench schema queries,
        disjoint from PostgreSQL-docs/Winand training docs).
      - PPL = exp(mean_cross_entropy).
      - Report all three PPLs, plus the pairwise deltas needed for K1868.
    """
    raise NotImplementedError(
        "K1868 PPL head-to-head depends on both Hedgehog and baseline adapters existing."
    )


# ─────────────────────────────────────────────
# Phase D: K1869 query-correctness auto-judge
# ─────────────────────────────────────────────

def measure_k1869_query_correctness_judge(student_adapter: Path,
                                          held_out: list) -> float:
    """K1869: blind-paired judge scores query-correctness of base vs Hedgehog-adapted
    answers on 50 held-out SQL-optimization prompts. Returns Δ =
    mean(score_hedgehog - score_base).

    Kill condition: Δ < +5 pp.

    NOT YET IMPLEMENTED. Requires:
      - generate(base, format_prompt(Q)) per held-out pair.
      - generate(base + Hedgehog, format_prompt(Q)) per held-out pair.
      - judge rubric (0-10): (a) correct join/index strategy identification for given
        cardinality scenario, (b) idiomatic construct preference (LATERAL vs correlated
        subquery, CTE vs derived table, window function vs self-join, EXPLAIN ANALYZE
        justification), (c) SQL syntactic + semantic validity — PostgreSQL dry-run
        EXPLAIN on reference schemas (pgbench + Spider 2.0) as hard-floor: parse/plan
        failure → rubric correctness axis = 0.
      - Blind-paired presentation to judge to reduce absolute-score bias.
      - Report mean(score_hedgehog - score_base) as a pp-delta on the 0–100 rescaled
        rubric.
    """
    raise NotImplementedError(
        "K1869 judge eval depends on Hedgehog adapter existing and PostgreSQL "
        "reference-schema test harness."
    )


# ─────────────────────────────────────────────
# Main
# ─────────────────────────────────────────────

def main():
    t_start = time.time()
    log_memory("start")

    try:
        import mlx_lm
        mlx_lm_version = getattr(mlx_lm, "__version__", "unknown")
    except Exception:
        mlx_lm_version = "import_failed"

    print("Hedgehog SQL-domain adapter distillation (Gemma 4 E4B ← 26B)", flush=True)
    print(f"SMOKE_TEST={IS_SMOKE}, N_TRAIN={N_TRAIN}, N_HELDOUT={N_HELDOUT}, N_STEPS={N_STEPS}",
          flush=True)
    print(f"mlx-lm version: {mlx_lm_version}", flush=True)

    data_dir = EXPERIMENT_DIR / "data" / "sql_qa_pairs"
    adapters_dir = EXPERIMENT_DIR / "adapters"
    student_adapter = adapters_dir / "hedgehog_sql_r8"
    baseline_adapter = adapters_dir / "token_lora_sql_r8"

    results = {
        "is_smoke": IS_SMOKE,
        "n_train": N_TRAIN,
        "n_heldout": N_HELDOUT,
        "n_steps": N_STEPS,
        "student_model": STUDENT_MODEL,
        "teacher_model": TEACHER_MODEL,
        "mlx_lm_version": mlx_lm_version,
        "adapter_rank": ADAPTER_RANK,
        "adapter_targets": list(ADAPTER_TARGETS),
        "lora_scale": LORA_SCALE,
        "seqlen": SEQLEN,
        "enable_thinking": ENABLE_THINKING,
        "sql_focus_topics": list(SQL_FOCUS_TOPICS),
        "phase_0_dataset": None,
        "phase_b_student_train": None,
        "phase_baseline_token_lora": None,
        "phase_c_k1868": {},
        "phase_d_k1869": {},
        "kc": {
            "K1868_hedgehog_ppl_not_greater_than_generic_lora": "untested",
            "K1869_query_correctness_delta_ge_5pp": "untested",
        },
        "verdict": "PROVISIONAL",
        "all_pass": False,
        "blockers": [],
    }

    # ── Phase 0: dataset ────────────────────────────────────
    print("\n=== Phase 0: PostgreSQL docs + Winand + Spider 2.0/BIRD-SQL corpus + Q-A curation ===", flush=True)
    try:
        results["phase_0_dataset"] = prepare_sql_qa_pairs(data_dir, N_TRAIN, N_HELDOUT)
    except NotImplementedError as exc:
        results["phase_0_dataset"] = {"not_implemented": str(exc)}
        results["blockers"].append(f"Phase 0 not implemented: {exc}")
    except Exception as exc:
        results["phase_0_dataset"] = {"error": str(exc)}
        results["blockers"].append(f"Phase 0 failed: {exc}")

    # ── Phase B: Hedgehog student training ──────────────────
    try:
        phase_b = train_hedgehog_student(data_dir, student_adapter)
        results["phase_b_student_train"] = phase_b
    except NotImplementedError as exc:
        results["phase_b_student_train"] = {"not_implemented": str(exc)}
        results["blockers"].append(f"Phase B not implemented: {exc}")
    except Exception as exc:
        results["phase_b_student_train"] = {"error": str(exc)}
        results["blockers"].append(f"Phase B failed: {exc}")

    # ── Phase Baseline: generic token-space LoRA ────────────
    try:
        phase_baseline = train_generic_token_lora_baseline(data_dir, baseline_adapter)
        results["phase_baseline_token_lora"] = phase_baseline
    except NotImplementedError as exc:
        results["phase_baseline_token_lora"] = {"not_implemented": str(exc)}
        results["blockers"].append(f"Baseline not implemented: {exc}")
    except Exception as exc:
        results["phase_baseline_token_lora"] = {"error": str(exc)}
        results["blockers"].append(f"Baseline failed: {exc}")

    # ── Phase C: K1868 PPL head-to-head ─────────────────────
    if student_adapter.exists() and baseline_adapter.exists():
        try:
            k1868 = measure_k1868_ppl(student_adapter, baseline_adapter, sql_eval_text=[])
            results["phase_c_k1868"] = k1868
        except NotImplementedError as exc:
            results["blockers"].append(f"K1868 not implemented: {exc}")
        except Exception as exc:
            results["blockers"].append(f"K1868 failed: {exc}")
    else:
        results["blockers"].append(
            "Phase C skipped: Hedgehog and/or baseline adapter not produced "
            "(Phase B + Baseline blockers)"
        )

    # ── Phase D: K1869 query-correctness judge ──────────────
    if student_adapter.exists():
        try:
            k1869 = measure_k1869_query_correctness_judge(student_adapter, held_out=[])
            results["phase_d_k1869"]["K1869_query_correctness_delta_pp"] = round(float(k1869), 2)
        except NotImplementedError as exc:
            results["blockers"].append(f"K1869 not implemented: {exc}")
        except Exception as exc:
            results["blockers"].append(f"K1869 failed: {exc}")
    else:
        results["blockers"].append(
            "Phase D skipped: Hedgehog adapter not produced (Phase B blocker)"
        )

    # ── KC resolution ──────────────────────────────────────
    # With Phase 0 / Phase B / Baseline not implemented, both KCs remain "untested".
    # When the _impl follow-up lands, populate kc dict from phase_c_k1868 + phase_d_k1869
    # using the F#666 verdict matrix (see MATH.md §4).

    results["total_time_s"] = round(time.time() - t_start, 1)

    RESULTS_FILE.write_text(json.dumps(results, indent=2))
    print("\n" + "=" * 60, flush=True)
    print("RESULTS SUMMARY", flush=True)
    print("=" * 60, flush=True)
    print(f"Verdict: {results['verdict']}", flush=True)
    print(f"KCs: {json.dumps(results['kc'])}", flush=True)
    print(f"Blockers ({len(results['blockers'])}):", flush=True)
    for b in results["blockers"]:
        print(f"  - {b}", flush=True)
    print(f"Total time: {results['total_time_s']:.1f}s", flush=True)


if __name__ == "__main__":
    main()
